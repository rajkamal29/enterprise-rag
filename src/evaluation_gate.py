"""CI evaluation gate for comparison outputs.

Reads ``data/track_compare.json`` (generated by ``src/compare_tracks.py``)
and fails if citation/relevance thresholds are not met.
"""

from __future__ import annotations

import argparse
import json
from dataclasses import dataclass
from pathlib import Path


@dataclass
class TrackStats:
    """Aggregated quality stats for one track."""

    records: int = 0
    cited: int = 0
    relevance_sum: float = 0.0

    @property
    def citation_rate(self) -> float:
        if self.records == 0:
            return 0.0
        return self.cited / self.records

    @property
    def avg_relevance(self) -> float:
        if self.records == 0:
            return 0.0
        return self.relevance_sum / self.records


def _as_bool(value: object) -> bool:
    if isinstance(value, bool):
        return value
    if isinstance(value, str):
        return value.strip().lower() in {"1", "true", "yes", "y"}
    return False


def _as_float(value: object) -> float:
    if isinstance(value, int | float):
        return float(value)
    if isinstance(value, str):
        try:
            return float(value)
        except ValueError:
            return 0.0
    return 0.0


def load_records(path: Path) -> list[dict[str, object]]:
    """Load and validate JSON comparison records."""
    if not path.exists():
        raise FileNotFoundError(f"Comparison file not found: {path}")

    with path.open("r", encoding="utf-8") as f:
        raw = json.load(f)

    if not isinstance(raw, list):
        raise ValueError("Comparison JSON must be a list of records")

    records: list[dict[str, object]] = []
    for item in raw:
        if isinstance(item, dict):
            records.append(item)
    return records


def summarize(records: list[dict[str, object]]) -> dict[str, TrackStats]:
    """Aggregate per-track metrics from flat records."""
    stats: dict[str, TrackStats] = {}
    for rec in records:
        track = str(rec.get("track_name", "unknown"))
        if track not in stats:
            stats[track] = TrackStats()

        stats[track].records += 1
        if _as_bool(rec.get("has_citation")):
            stats[track].cited += 1
        stats[track].relevance_sum += _as_float(rec.get("relevance_score"))

    return stats


def evaluate(
    stats: dict[str, TrackStats],
    *,
    min_records: int,
    min_citation_rate: float,
    min_avg_relevance: float,
) -> list[str]:
    """Return gate failures as human-readable messages."""
    failures: list[str] = []

    if not stats:
        failures.append("No track records found")
        return failures

    for track, track_stats in stats.items():
        if track_stats.records < min_records:
            failures.append(
                f"{track}: records {track_stats.records} < required {min_records}"
            )
        if track_stats.citation_rate < min_citation_rate:
            failures.append(
                f"{track}: citation_rate {track_stats.citation_rate:.3f} "
                f"< required {min_citation_rate:.3f}"
            )
        if track_stats.avg_relevance < min_avg_relevance:
            failures.append(
                f"{track}: avg_relevance {track_stats.avg_relevance:.3f} "
                f"< required {min_avg_relevance:.3f}"
            )

    return failures


def parse_args() -> argparse.Namespace:
    """Parse CLI args."""
    parser = argparse.ArgumentParser(description="Run evaluation quality gate")
    parser.add_argument("--input", default="data/track_compare.json")
    parser.add_argument("--min-records", type=int, default=3)
    parser.add_argument("--min-citation-rate", type=float, default=0.60)
    parser.add_argument("--min-avg-relevance", type=float, default=0.70)
    return parser.parse_args()


def main() -> None:
    """Entry point for CI."""
    args = parse_args()
    input_path = Path(args.input)

    records = load_records(input_path)
    stats = summarize(records)

    print("Evaluation Gate Summary")
    for track, track_stats in stats.items():
        print(
            f"- {track}: records={track_stats.records}, "
            f"citation_rate={track_stats.citation_rate:.3f}, "
            f"avg_relevance={track_stats.avg_relevance:.3f}"
        )

    failures = evaluate(
        stats,
        min_records=args.min_records,
        min_citation_rate=args.min_citation_rate,
        min_avg_relevance=args.min_avg_relevance,
    )
    if failures:
        print("\nEvaluation gate FAILED")
        for failure in failures:
            print(f"- {failure}")
        raise SystemExit(1)

    print("\nEvaluation gate PASSED")


if __name__ == "__main__":
    main()
